CREATE SEQUENCE seq_contract;

CREATE TABLE contract
(
    id                 BIGINT UNIQUE NOT NULL DEFAULT NEXTVAL('seq_contract'),
    fk_job             BIGINT        NOT NULL,
    fk_employee        BIGINT        NOT NULL,
    fk_employer        BIGINT        NOT NULL,
    fk_contract_status SMALLINT,



































































































































































































































































































































































    number             VARCHAR(45)  NOT NULL,
    base_salary        DOUBLE PRECISION,
    fixed_term         BOOLEAN,
    has_errors         BOOLEAN       NOT NULL DEFAULT FALSE,
--     version       INTEGER       NOT NULL DEFAULT 0,
    created_at         TIMESTAMPTZ   NOT NULL DEFAULT NOW()::TIMESTAMPTZ,
    updated_at         TIMESTAMPTZ   NULL,

    CONSTRAINT contract_pk_id PRIMARY KEY (id),
    CONSTRAINT contract_fk_employee_fk FOREIGN KEY (fk_employee) REFERENCES app_user (id),
    CONSTRAINT contract_fk_job FOREIGN KEY (fk_job) REFERENCES job (id),
    CONSTRAINT contract_fk_employer FOREIGN KEY (fk_employer) REFERENCES employer (id),
    CONSTRAINT contract_fk_contract_status FOREIGN KEY (fk_contract_status) REFERENCES contract_status (id)
);
